def multiply_int(num: int) -> int:
    """
    This function takes an integer as input and returns the result of multiplying it by 2.

    Parameters:
    num (int): The integer that will be multiplied by 2.

    Returns:
    int: The result of multiplying the input integer by 2.

    Implementation:
    The function first declares a variable `multiplied_num` and assigns it the value of `num` multiplied by 2.
    The function then returns `multiplied_num`.
    """
    multiplied_num = num * 2
    return multiplied_num
