def multiply_number(number: float) -> float:
    """
    This function takes a single numerical argument, `number`, and returns the result of multiplying it by 2.
    While the function is defined with a float input type, it can also accept integer inputs.

    :param number: the number to be multiplied
    :type number: float or int
    :return: the multiplied number
    :rtype: float
    """
    multiplied_number = number * 2
    return multiplied_number
